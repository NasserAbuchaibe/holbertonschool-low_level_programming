#include "3-calc.h"
#include <stdio.h>


/**
 * op_add - add 2 number
 * @a: number 1
 * @b: number 2
 * return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}


int op_sub(int a, int b)
{
	return (a - b);
}


int op_mul(int a, int b)
{
	return (a * b);
}


int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
