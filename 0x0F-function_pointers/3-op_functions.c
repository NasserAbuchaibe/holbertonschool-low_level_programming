#include "3-calc.h"
#include <stdio.h>


/**
 * op_add - add 2 number
 * @a: number 1
 * @b: number 2
 * return: number int res.
 */
int op_add(int a, int b)
{
	return (a + b);
}



/**
 * op_sub - sub 2 number
 * @a: number 1
 * @b: number 2
 * return: number int.
 */
int op_sub(int a, int b)
{
	return (a - b);
}



/**
 * op_mul - mul 2 number
 * @a: number 1
 * @b: number 2
 * return: number int.
 */
int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * op_div - div 2 number
 * @a: number 1
 * @b: number 2
 * return: number int.
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - mod 2 number
 * @a: number 1
 * @b: number 2
 * return: number int.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
